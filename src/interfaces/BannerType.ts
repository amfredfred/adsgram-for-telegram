/**
 * `BannerType` is a union type that represents the different types of ad banners available.
 * 
 * - `'RewardedVideo'`: Represents a video ad that rewards the user for watching.
 * - `'FullscreenMedia'`: Represents a full-screen media ad.
 * 
 */
export type BannerType = 'RewardedVideo' | 'FullscreenMedia';